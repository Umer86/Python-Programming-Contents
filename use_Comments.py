"""The input() function allows user input.
So we will take input from user and store it in a dynamically typed variable, called input_msg"""

"""A language is dynamically-typed if the type of a variable is checked during run-time. ...
In Dynamically typed languages, variables are bound to objects at run-time by means of assignment statements"""

input_msg=input("Type anything:  ")

print("Hello, {}".format(str(input_msg)))
